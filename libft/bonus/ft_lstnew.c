#include "libft.h"

t_list	*ft_lstnew(void *new_content)
{
	t_list	*new_list;

	new_list = (t_list *)malloc(sizeof(t_list));
	if (!new_list)
		return (NULL);
	new_list->content = new_content;
	new_list->next = NULL;
	return (new_list);
}
