#include "ft_printf.h"

int	is_not_zero_format_flag(int c)
{
	return (c == '-' || c == '#' || c == ' ' || c == '+');
}

int	is_format_flag(int c)
{
	return (c == '0' || c == '-' || c == '#' || c == ' ' || c == '+');
}
